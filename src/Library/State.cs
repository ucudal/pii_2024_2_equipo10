namespace Library;

/// <summary>
/// Representa los estados negativos que puede tener un Pokemon.
/// </summary>
public enum State
{
    /// <summary>
    /// Estado dormido
    /// </summary>
    Asleep,
    /// <summary>
    /// Estado paralizado
    /// </summary>
    Paralized,
    /// <summary>
    /// Estado envenenado
    /// </summary>
    Poisoned,
    /// <summary>
    /// Estado Quemado
    /// </summary>
    Burned
}